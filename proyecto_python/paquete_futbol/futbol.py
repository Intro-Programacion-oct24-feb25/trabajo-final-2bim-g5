def registrar_participante_futbol():
    nombre = input("Nombre del participante: ")
    edad = input("Edad: ")
    posicion = input("Posición preferida: ")
    ciudad = input("Ciudad: ")
    equipo = input("Equipo favorito: ")

    print(f"\nParticipante registrado en Fútbol:")
    print(f"Nombre: {nombre}, Edad: {edad}, Posición: {posicion}, Ciudad: {ciudad}, Equipo favorito: {equipo}")
